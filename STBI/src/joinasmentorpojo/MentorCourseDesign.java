package joinasmentorpojo;

public class MentorCourseDesign {

	
	private String mentorcourseid;
	private String coursename;
	private String description;
	private String couserlevel;
	private String prerequisite;
	private String duration;
	private String weekmonth;
	private String coursecredit;
	private String courseobjective;
	private String courseoutcomes;
	private String coursemodule;
	private String coursetool;
	private String referencebook;
	public MentorCourseDesign(String mentorcourseid, String coursename, String description, String couserlevel,
			String prerequisite, String duration, String weekmonth, String coursecredit, String courseobjective,
			String courseoutcomes, String coursemodule, String coursetool, String referencebook) {
		super();
		this.mentorcourseid = mentorcourseid;
		this.coursename = coursename;
		this.description = description;
		this.couserlevel = couserlevel;
		this.prerequisite = prerequisite;
		this.duration = duration;
		this.weekmonth = weekmonth;
		this.coursecredit = coursecredit;
		this.courseobjective = courseobjective;
		this.courseoutcomes = courseoutcomes;
		this.coursemodule = coursemodule;
		this.coursetool = coursetool;
		this.referencebook = referencebook;
	}
	
	
	
}
