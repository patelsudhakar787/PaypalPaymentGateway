package joinasmentorpojo;

public class MentorBasicDetail {
	
	private String firstname;
	private String middlename;
	private String lastname;
	private String dob;
	private String gender;
	private String contact;
	private String emailid;
	private String adharno;
	private String temporaryaddress;
	private String city;
	private String pincode;
	public MentorBasicDetail(String firstname, String middlename, String lastname, String dob, String gender,
			String contact, String emailid, String adharno, String temporaryaddress, String city, String pincode) {
		super();
		this.firstname = firstname;
		this.middlename = middlename;
		this.lastname = lastname;
		this.dob = dob;
		this.gender = gender;
		this.contact = contact;
		this.emailid = emailid;
		this.adharno = adharno;
		this.temporaryaddress = temporaryaddress;
		this.city = city;
		this.pincode = pincode;
	}
	
	
	

}
